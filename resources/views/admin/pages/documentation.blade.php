                        <h2 id="chapter_1">Панель администрирования</h2>

                        <p>Панель администрирования позволяет редактировать данные сайта (курсы, подписки, словари и
                            т.д.).
                            Доступ в панель имеют пользователи с ролью <b>Администратор</b>.</p>
                        <p>
                            Для создания новых записей (доступно не для всех сущностей) необходимо нажать кнопку
                            <button type="button" class="btn">Добавить</button>
                            . Для редактирования записей необходимо нажать кнопку
                            <button type="button" class="btn">Редактировать</button>
                            . Для удаления (доступно не для всех сущностей) необходимо нажать кнопку
                            <button type="button" class="btn btn-secondry">Удалить</button>
                            . Неактивные элементы отображаются в списке серым цветом.
                        </p>

                        <h3 id="section_1_1">Курсы</h3>

                        <p>Раздел <a href="{{ route('courses.index') }}">Курсы</a> позволяет редактировать
                            информацию о курсах. Чтобы выгрузить курсы (название и настройки видимости) из LMS,
                            необходимо
                            нажать кнопку
                            <button type="button" class="btn">Синхронизировать</button>
                            .
                            Курсы категории <b>Пробный курс</b> не синхронизируются.
                        </p>

                        <p>Курс должен содержать описание и изображение, название синхронизируется с LMS. Для
                            форматирования
                            описания необходимо использовать
                            текстовый редактор, после сохранения рекомендуется проверить корректность отображения курса
                            на
                            сайте. Изображение курса не синхронизируется с LMS.</p>

                        <h3 id="section_1_2">Подписки</h3>

                        <p>Раздел <a href="{{ route('subscriptions.index') }}">Подписки</a> позволяет редактировать
                            информацию о планах подписок. К подписке необходимо добавить название и краткое описание.
                            План
                            подписки включает в себя количество звонков, стоимость в рублях, срок действия подписки в
                            месяцах и доступные курсы, на которые будет зачислен пользователь после покупки подписки.
                        </p>

                        <p>Флаг <b>Aктивен</b> отображает подписку на сайте. Деактивированные планы продолжают быть
                            доступны
                            подписанным на них пользователям.</p>

                        <h3 id="section_1_3">Словари</h3>

                        <p>
                            Раздел <a href="{{ route('topics.index') }}">Словари</a> позволяет формировать словари из
                            слов.
                            Словарь должен иметь название и изображение, доступные слова можно выбрать из списка (чтобы
                            выбрать несколько, необходимо зажать Ctrl).
                        </p>

                        <p>
                            Активные словари отображаются на сайте и доступны для тренировок.
                        </p>

                        <h3 id="section_1_4">Слова</h3>

                        <p>
                            Раздел <a href="{{ route('words.index') }}">Слова</a> позволяет создавать новые слова и
                            прикреплять их к словарям. Видео жестов связаны с регионами. Чтобы добавить новое видео,
                            необходимо
                            <button type="button" class="btn">Очистить форму</button>
                            , выбрать из списка доступный регион и загрузить видео в формате mp4, затем нажать
                            <button type="button" class="btn">Добавить</button>
                            . Для
                            редактирования слова в регионе необходимо кликнуть на интересующий регион, для удаления -
                            нажать
                            на <b>✖</b>.
                        </p>

                        <p>
                            Перед добавлением регионов к новому слову его необходимо
                            <button type="button" class="btn">Сохранить</button>
                            .
                        </p>

                        <h3 id="section_1_5">Регионы</h3>

                        <p> Раздел <a href="{{ route('regions.index') }}">Регионы</a> позволяет создавать новые регионы.
                            Регион должен иметь название в именительном и предложном падеже. Для сохранения координат
                            необходимо найти на карте географический объект и кликнуть правой кнопкой мыши. Над картой
                            отобразятся его долгота и широта.</p>

                        <h3 id="section_1_6">Администраторы</h3>

                        <p>Раздел <a href="{{ route('admins.index') }}">Администраторы</a> позволяет добавлять новых
                            администраторов. Адрес электронной почты администратора должен быть уникальным, т.е.
                            использовать email существующего пользователя нельзя. Выключенный флаг <b>Активен</b> не
                            позволит пользователю войти в панель администрирования. Администраторам доступен интерфейс
                            пользователей.</p>

                        <h3 id="section_1_7">Уведомления</h3>

                        <p>Раздел <a href="{{ route('notifications.create') }}">Уведомления</a> позволяет создавать
                            уведомления для пользователей при обновлении контента. Можно создать уведомления для курсов,
                            планов подписок и словарей по отдельности или сразу по трем категориям.</p>

                        <p>В строке отображается текст уведомления, его можно изменить. Тег <b>:name</b> будет заменен
                            на
                            название курса, плана или словаря.
                            <input type="text" name="topic_msg"
                                   value="По теме &laquo;:name&raquo; появились новые слова." required
                                   class="form-control"/>
                        </p>

                        <p>
                            Из списка можно выбрать один или несколько курсов, зарегистрированным на
                            курс пользователям будут отправлены уведомления. Аналогично с подписками и словарями.
                        </p>

                        <h3 id="section_1_8">Звонки</h3>

                        <p>Раздел <a href="{{ route('calls.index') }}">Звонки</a> позволяет просматривать
                            информацию о запрошенных звонках. Звонок может иметь два статуса: Запрошен или Удовлетворен.
                            Администратор может изменить статус звонка, информация о времени и администраторе будет
                            сохранена и отображена пользователю. В общем списке удовлетворенные запросы отображаются
                            серым
                            цветом.</p>

                        <h3 id="section_1_9">Константы</h3>

                        <p>Раздел <a href="{{ route('constants.edit') }}">Константы</a> предназначен для редактирования
                            значений,
                            используемых на сайте.
                        </p>

                        <p>
                            Константы <b>Число повторений слова в тесте</b> настраивают тренировочные тесты. Каждое
                            слово
                            должно быть верно отмечено в тесте определенного типа, чтобы считаться выученным.
                        </p>

                        <h3 id="section_1_10">Переход в moodle и в профиль</h3>

                        <p>Пункты <a href="{{ route('profile.courses.study') }}">В moodle</a> и <a
                                    href="{{ route('profile.courses') }}">В профиль</a> можно использовать для перехода
                            в
                            LMS и профиль пользователя соответственно.
                        </p>
