                        <h2 id="chapter_4">LMS Moodle</h2>

                        <p>В LMS Moodle пользователи проходят обучающие курсы. Доступ зарегистрированным пользователям
                            предоставляется автоматически.</p>

                        <h3 id="section_4_1">Администраторы</h3>

                        <p>Управлять материалами курсов могут администраторы, в системе moodle пользователям необходимо
                            вручную выдать соответствующие права в разделе Администрирование / Пользователи / Права / <a
                                    href="http://moodle.signlingua.ru/admin/roles/admins.php">Администраторы сайта</a>.
                        </p>

                        <h3 id="section_4_2">Создание курсов</h3>

                        <p> Создать новый курс можно в разделе Администрирование / Курсы / Курсы / <a
                                    href="http://moodle.signlingua.ru/course/edit.php?category=0">Добавить курс</a>.
                            Новый курс должен иметь полное и короткое название, для курсов, доступных по подписки,
                            категория <b>Платный курс</b>, видимость - <b>Показать</b>, для даты окончания необходимо
                            снять флаг <b>Включено</b>. Дополнительные параметры можно посмотреть в <a
                                    href="https://docs.moodle.org/403/en/Courses">документации moodle</a>.
                        </p>

                        <p>Курсы категории <b>Пробный курс</b> доступны всем пользователям по умолчанию.</p>

