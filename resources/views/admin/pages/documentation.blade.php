                        <h2 id="chapter_2">Основные функции</h2>

                        <h3 id="section_2_1">Курсы</h3>

                        <p>В разделе <a href="{{ route('courses') }}">Курсы</a> пользователи могут посмотреть информацию
                            о всех доступных курсах. Для получения к ним доступа необходимо зарегистрироваться, а затем
                            приобрести подписку. Тогда пользователь будет зарегистрирован на все доступные по подписке
                            курсы. Перейти в LMS для прохождения обучения можно в профиле в разделе <a
                                    href="{{ route('profile.courses') }}">Курсы</a>.
                        </p>

                        <h3 id="section_2_2">Подписки</h3>

                        <p>В разделе <a href="{{ route('subscriptions') }}">Подписки</a> пользователи могут посмотреть
                            информацию
                            о всех доступных планах подписок. Каждый пользователь может иметь лишь один текущий план.
                            Информацию по текущему плану можно просмотреть в профиле в разделе <a
                                    href="{{ route('profile.subscription') }}">Подписки</a>. План подписки включает
                            доступ к нескольким или всем курсам и некоторое число звонков. Через установленное число
                            месяцев подписка автоматически продлевается, если пользователь не откажется от
                            автопродления.
                        </p>

                        <h3 id="section_2_3">Словари</h3>

                        <p>В разделе <a href="{{ route('topics') }}">Словари</a> пользователи могут просматривать
                            материалы, упорядоченные по темам. Внутри словаря доступен поиск по словам. Чтобы
                            просмотреть, какие жесты используются для обозначения слова в разных регионах, необходимо
                            кликнуть по нему. Справа на карте появятся метки регионов, кликнув по ним, можно открыть
                            видео с жестом.
                        </p>

                        <h3 id="section_2_4">Контакты</h3>

                        <p>В разделе <a href="{{ route('feedback') }}">Контакты</a> пользователи могут отправить
                            сообщение в поддержку. На email, указанный в <a href="#section_1_9">Константах</a>, придет
                            письмо с вопросом и данными пользователя. Если пользователь не зарегистрирован,
                            контактную информацию ему потребуется указать вручную.
                        </p>

                        <h3 id="section_2_5">Вход / Регистрация</h3>

                        <p><a href="{{ route('login') }}">Войти</a> или <a href="{{ route('register') }}">Зарегистрироваться</a>
                            можно в соответствующих разделах, в качестве логина используется email (должен быть уникален
                            для всех). Пользователи могут войти через Яндекс или Google. После регистрации на указанный
                            адрес электронной почты пользователю будет отправлено письмо со ссылкой для подтверждения.
                            Пользователям, входящим через Яндекс или Google, подтверждать почту не требуется.
                        </p>

                        <h3 id="section_2_6">Восстановление пароля</h3>

                        <p>В разделе <a href="{{ route('password.request') }}">Не помню пароль</a> пользователь может
                            указать свой адрес электронной почты, чтобы получить ссылку для сброса пароля.</p>

