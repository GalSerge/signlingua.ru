                        <h2 id="chapter_3">Личный кабинет</h2>

                        <h3 id="section_3_1">Мои курсы</h3>

                        <p>В разделе <a href="{{ route('profile.courses') }}">Мои курсы</a> пользователю отображаются
                            доступные ему по подписке курсы с прогрессом обучения. Здесь же ссылка <a class="btn"
                                                                                                      href="{{ route('profile.courses.study') }}">Продолжить
                                обучение</a> для перехода в LMS.
                            Пользователям без подписки доступны курсы категории <b>Пробный курс</b>.</p>

                        <h3 id="section_3_2">Прогресс</h3>

                        <p>В разделе <a href="{{ route('training.index') }}">Прогресс</a> пользователю отображаются
                            словари с прогрессом по обучению. У авторизованных пользователей есть возможность
                            тренировать знания жестов по темам словарей. Каждый тур тренировок состоит из 10-15 вопросов
                            трех типов (см. <a href="#section_1_9">Константы</a>).</p>

                        <h3 id="section_3_3">Подписка</h3>

                        <p>В разделе <a href="{{ route('profile.subscription') }}">Подписка</a> отображается информация
                            по текущей подписке.</p>

                        <h3 id="section_3_4">Финансы</h3>

                        <p>В разделе <a href="{{ route('payments.index') }}">Финансы</a> отображается история платежей.
                            Платеж может иметь следующий статус:
                        <ul>
                            <li>Создан - временный статус, если платеж не завершен, переходит в статус Отменен</li>
                            <li>Ожидает подтверждения - временный статус, если платеж завершен, но подтверждение о зачисление не получено</li>
                            <li>Успешно завершен - платеж завершен, приход средств подтвержден</li>
                            <li>Отменен - платеж не удался</li>
                            <li>Успешно возвращен - тестовые платежи возвращаются пользователю</li>
                            <li>Возврат не удался - не удалось вернуть тестовый платеж</li>
                        </ul>

                        <p>
                            Также здесь пользователь имеет возможность изменить сохраненный способ оплаты и отменить
                            автопродление.
                        </p>

                        <h3 id="section_3_5">Звонки</h3>

                        <p>В разделе <a href="{{ route('profile.calls') }}">Звонки</a> отображаются доступные звонки,
                            число совершенных звонков, история звонков.
                            Пользователь имеет возможность запросить звонок с тьютором, если нет запрошенных звонко, и
                            купить несколько звонков дополнительно.
                        </p>

                        <h3 id="section_3_6">Редактировать профиль</h3>

                        <p>В разделе <a href="{{ route('profile.edit') }}">Редактировать профиль</a> пользователь может
                            изменить личные данные (имя, фамилию и аватар). Если пользователь регистрировался вручную,
                            ему доступна возможность сменить пароль в разделе <a href="{{ route('password.edit') }}">Сменить
                                пароль</a>.
                        </p>
